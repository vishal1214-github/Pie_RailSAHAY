package model;

public class Counts {
    public Counts() {
        super();
    }
    
    private String fnrCont;
    private String odrCont;
    private String srCont;
    private String cnrnCont;

    public void setFnrCont(String fnrCont) {
        this.fnrCont = fnrCont;
    }

    public String getFnrCont() {
        return fnrCont;
    }

    public void setOdrCont(String odrCont) {
        this.odrCont = odrCont;
    }

    public String getOdrCont() {
        return odrCont;
    }

    public void setSrCont(String srCont) {
        this.srCont = srCont;
    }

    public String getSrCont() {
        return srCont;
    }

    public void setCnrnCont(String cnrnCont) {
        this.cnrnCont = cnrnCont;
    }

    public String getCnrnCont() {
        return cnrnCont;
    }


}
